#1011
#문자형(char)으로 변수를 하나 선언하고, 변수에 문자를 저장한 후 변수에 저장되어 있는 문자를 그대로 출력해보자.
a = input();
print(a);

#1012
#실수형(float)로 변수를 선언하고 그 변수에 실수값을 저장한 후 저장되어 있는 실수값을 출력해보자.
a = float(input());
print("%f" % a);

#1013
#정수(int) 2개를 입력받아 그대로 출력해보자. 2개의 정수가 공백으로 구분되어 입력된다.
a,b = input().split()
print(a,b)

#1014
#2개의 문자(ASCII CODE)를 입력받아서 순서를 바꿔 출력해보자. 2개의 정수가 공백으로 구분되어 입력된다.
a,b = input().split()
