#1018
#어떤 형식에 맞추어 시간이 입력될 때, 그대로 출력하는 연습을 해보자.
#시(hour)와 분(minute)이 ":" 으로 구분되어 입력된다.
a , b= input().split(":")
print(a,b,sep=":")


#1019
#년, 월, 일을 입력받아 지정된 형식으로 출력하는 연습을 해보자.
#연, 월, 일이 ".(닷)"으로 구분되어 입력된다.
a , b, c= input().split(".")
print(a.zfill(4),b.zfill(2),c.zfill(2),sep=".")

