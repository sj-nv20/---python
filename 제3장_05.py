#기본 데이터형


#[숫자형]

#정수형
a = 123     
print(type(a))  #<class 'int'>

##Python에서는 int 크기 제한이 없음
a = 100 ** 100
print(a)
##정수형에는 16진수, 10진수, 8진수, 2진수도 사용 가능
a = 0xFF    #16진수
b = 0o77    #8진수
c = 0b1111  #2진수
print(a,b,c)

#실수형 - 소수점이 있는 데이터
a = 3.14        
b = 3.14e5
print(type(a))  #<class 'float'>
print(type(b))  #<class 'float'>

##정수 및 실수형 데이터는 사칙연산(+, -, *, /) 수행 가능
a = 10
b = 20
print(a+b, a-b, a*b, a/b)      # 30 -10 200 0.5

##제곱을 의미하는 **, 나머지를 구하는 %, 나눈 후 소수점을 버리는 // 연산자도 사용 가능
a, b = 9, 2
print(a**b, a%b, a//b)      # 81 1 4

