#[불형] - 참이나 거짓만 저장 할 수 있는 논리형

a = True
print(type(a))  #<class 'bool'>

##불형은 비교의 결과를 참이나 거짓으로 저장하는데도 사용된다
a = (10 == 10)
b = (10 > 100)
print(a, b)     # True False

#----------------------------------------------------------------------------------------

#[문자열] - 문자 집합을 의미 / ""나 ''로 감싸야 한다.

a = "파이썬 만세"
a                   #'파이썬 만세'
print(a)            #파이썬 만세
print(type(a))      #<class 'str'>

##문자열 중간에 작은따옴표나 큰따옴표를 출력하고 싶다면 다른 따옴표로 묶어주면 된다
##또는 역슬래시(\)뒤에 큰따옴표나 작은따옴표를 붙이면 된다
a = "작은따옴표는 '모양이다."   
b = '큰따옴표는 "모양이다.'
print(a, b)                 #작은따옴표는 '모양이다. 큰따옴표는 "모양이다.

a = "이건 큰 따옴표 \"모양."
b = '이건 작은 따옴표 \'모양.'
print(a, b)                 #이건 큰 따옴표 "모양. 이건 작은 따옴표 '모양.

##문자열을 여러 줄로 놓으려면 \n을 넣어주거나 따옴표 3개를 연속해서 묶어줘도 된다
a = "파이썬\n만세"
print(a)        #파이썬
                #만세

a = """파이썬
만세"""
print(a)        #파이썬
                #만세
